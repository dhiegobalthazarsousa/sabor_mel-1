    private void gravarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gravarMouseClicked
        Validacao valida = new Validacao();
        
        URL resource = Principal.class.getResource("/empresa/");
        
        if(!labelNomeArquivo.getText().equals("...")){
            if(valida.validaCnpj(cnpj.getText())){
                if(nome.getText().length() > 5){
                    if(endereco.getText().length() > 5){
                        if(valida.validaDdd(ddd.getText())){
                            if(valida.validaTelefone(telefone.getText())){
                                
                                FileInputStream origem;
                                FileOutputStream destino;
                                FileChannel fcOrigem;
                                FileChannel fcDestino;

                                try {
                                    if(!caminhoArquivo.equals("")){
                                        origem  = new FileInputStream(caminhoArquivo);
                                        destino = new FileOutputStream(Paths.get(resource.toURI()).toFile()+ "/" +labelNomeArquivo.getText());

                                        fcOrigem = origem.getChannel();
                                        fcDestino = destino.getChannel();

                                        fcOrigem.transferTo(0, fcOrigem.size(), fcDestino);

                                        origem.close();
                                        destino.close();
                                    }

                                    /*Escrita do arquivo de texto*/
                                    File file = new File(resource.getPath()+"dados.txt");
                                    BufferedWriter writer = new BufferedWriter(new FileWriter(file));

                                    writer.write(labelNomeArquivo.getText());
                                    writer.newLine();
                                    writer.write(cnpj.getText());
                                    writer.newLine();
                                    writer.write(nome.getText());
                                    writer.newLine();
                                    writer.write(endereco.getText());
                                    writer.newLine();
                                    writer.write(ddd.getText());
                                    writer.newLine();
                                    writer.write(telefone.getText());
                                    writer.newLine();
                                    
                                    writer.flush();
                                    writer.close();

                                    lerDados();
                                    
                                    JOptionPane.showMessageDialog(null, "Dados Alterados com Sucesso!");

                                } 
                                 catch (FileNotFoundException ex) {
                                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                                } 
                                 catch (IOException ex) {
                                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                                } 
                                 catch (URISyntaxException ex) {
                                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                                }
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Telefone Requerido");
                                telefone.setText(null);
                                telefone.requestFocus();
                            }
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "DDD Requerido");
                            ddd.setText(null);
                            ddd.requestFocus();
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Endere√ßo Inv√°lido");
                        endereco.setText(null);
                        endereco.requestFocus();
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Nome Fantasia Inv√°lido");
                    nome.setText(null);
                    nome.requestFocus();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "CNPJ Inv√°lido");
                cnpj.setText(null);
                cnpj.requestFocus();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Voc√™ deve selecionar uma imagem");
        }
    }//GEN-LAST:event_gravarMouseClicked

    private void lerDados() {
        /*LER*/
        URL resource = Principal.class.getResource("/empresa/");
        try {
            FileReader fileReader = new FileReader(resource.getPath()+"dados.txt");
            BufferedReader reader = new BufferedReader(fileReader);
            
            String data = null;
            
            int count = 0;
            while((data = reader.readLine()) != null){
                switch(count){
                    case 0:
                        labelNomeArquivo.setText(data);
                        logo.setIcon(new javax.swing.ImageIcon(resource.getPath()+data));
                        break;
                    case 1:
                        cnpj.setText(data);
                        break;
                    case 2:
                        nome.setText(data);
                        break;
                    case 3:
                        endereco.setText(data);
                        break;
                    case 4:
                        ddd.setText(data);
                        break;
                    case 5:
                        telefone.setText(data);
                        break;
                }
                count++;
            }
            
            reader.close();
        }
        catch (FileNotFoundException ex) {
            Logger.getLogger(DadosEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (IOException ex) {
            Logger.getLogger(DadosEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private static String caminhoArquivo = "";
    private void logoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logoMouseClicked
        JFileChooser arquivo = new JFileChooser();

        int retorno = arquivo.showOpenDialog(arquivo);
        

        if (retorno == JFileChooser.APPROVE_OPTION) {
            caminhoArquivo = arquivo.getSelectedFile().getAbsolutePath();
            String nomeArquivo = arquivo.getSelectedFile().getName();
            File selecionado = arquivo.getSelectedFile();
            
            String extensao = getExtensaoArquivo(selecionado);
            
            /*Verifica o sistema operacional utilizado*/
            String sistema = System.getProperty("os.name");
            if(sistema.equals("Linux")){
                extensao = "jpg";
            }
           
            if(extensao.toLowerCase().equals("jpg") || extensao.toLowerCase().equals("png")){
                try {
                    BufferedImage original = ImageIO.read(new File(caminhoArquivo));

                    
                    labelNomeArquivo.setText(nomeArquivo);
                    
                    logo.setIcon(new javax.swing.ImageIcon(original));
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Arquivo n√£o suportado.");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Extens√£o n√£o permitida.\nApenas jpg ou png");
            } 
        }
    }//GEN-LAST:event_logoMouseClicked

